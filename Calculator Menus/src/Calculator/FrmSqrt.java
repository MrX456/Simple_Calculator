    private void btnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualsActionPerformed
        //If textbox is empty
       if(txtValue1.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "Empty fields not allowed!");
       }
       else {
           int x;
           x = Integer.parseInt(txtValue1.getText());
           double root = sqrt(x);
           String f = Double.toString(root);
           if(f.length() > 8)
               txtResult.setText(f.substring(0,7));
           else
               txtResult.setText(f);
           txtValue1.setEditable(false);
       }
    }//GEN-LAST:event_btnEqualsActionPerformed

